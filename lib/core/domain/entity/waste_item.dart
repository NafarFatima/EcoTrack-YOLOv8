class WasteItem {
  final String id;
  final String title;
  final String description;
  final String iconPath;
  final int colorValue;
  final int pointsPerKg;

  WasteItem({
    required this.id,
    required this.title,
    required this.description,
    required this.iconPath,
    required this.colorValue,
    required this.pointsPerKg,
  });
}
